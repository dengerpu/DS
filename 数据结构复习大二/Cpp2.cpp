/*入一个四位数，将其加密后输出。方法是将该数每一位上的数字加9，
然后除以10取余，做为该位上的新数字，最后将千位和十位上的数字互换，
百位和个位上的数字互换，组成加密后的新四位数。例如输入1257，经过加9取余后得到新数字0146，再经过两次换位后得到4601。*/
#include<stdio.h>
int main()
{
int n,a,b,c,d,sum=0;
scanf("%d",&n);
a=(n+9)%10;
b=(n/10+9)%10;
c=(n/100+9)%10;
d=(n/1000+9)%10;
sum+=b*1000+a*100+d*10+c;
printf("The encrypted number is %d\n",sum);
}